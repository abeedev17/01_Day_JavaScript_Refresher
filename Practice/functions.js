// Function Declaration
// Function without a parameter and return
function square() {
  let num = 2;
  let sq = num * num;
  console.log(sq);
}

square();

function addTwoNumbers() {
  let numOne = 10;
  let numTwo = 20;
  let sum = numOne + numTwo;

  console.log(sum);
}

addTwoNumbers();

function printFullName() {
  let firstName = "James";
  let lastName = "Harry";
  let space = " ";
  let fullName = firstName + space + lastName;
  console.log(fullName);
}

printFullName();
